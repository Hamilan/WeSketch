package sketching.widgets;

import java.util.ArrayList;

/**
 * This class generalizes the Container Widgets in the sketching system.
 * Objects of this class are sendable through the net with Kryonet.
 * @author Hamilan
 */
public abstract class ContainerWidget extends Widget {
	/**
	 * For the 1st version widgets won't be contained in Containers.  Containers will just appear visually.
	 */
	public ArrayList<Widget> children = new ArrayList<Widget>();
}
